def _is_common_lowercase(corpus, name):
    """True, если ВСЕ кириллические слова имени встречаются в тексте ответов со строчной буквы.
    Бренды-проперноуны (Topvisor, Спортмастер) строчными не пишут, а обычные слова (метод, действие,
    социальных сетях) — пишут. Это ловит концепт-слова, которых нет в стоп-списках, без перечисления."""
    if not corpus:
        return False
    cyr = [w.lower() for w in re.findall(r"[А-Яа-яЁё]{3,}", name)]      # слова из 3+ кириллических букв (любой регистр): «Все», «Мир», «Метод»
    if not cyr:
        return False                                                  # латиница/домены не трогаем
    # Ищем ВСЮ ФРАЗУ целиком со строчной буквы (а не слова по отдельности): концепт «проверка квалификации»
    # пишут строчными, а бренд «Гранд Мебель» — нет, даже если «мебель» где-то встречается строчным.
    parts = []
    for w in cyr:
        stem = re.escape(w[:-2]) if len(w) >= 8 else re.escape(w)      # отбрасываем падежное окончание: «проверку»→«провер»
        parts.append(stem + r"[а-яё]*")
    phrase = r"[\s\-]+(?:[а-яё]{1,4}[\s\-]+){0,2}".join(parts)         # допускаем короткие связки между словами («опыт и портфолио»)
    return bool(re.search(r"(?<![А-Яа-яЁёA-Za-z])" + phrase + r"(?![A-Za-z])", corpus))
