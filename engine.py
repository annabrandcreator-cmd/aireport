def _queries_cache_path(site):
    host = _host(site)
    d = os.environ.get("QUERIES_DIR") or os.environ.get("REPORTS_DIR") or "/tmp"
    return os.path.join(d, "qset_" + re.sub(r"[^a-z0-9.]", "_", host) + ".json")

_QSET_VER = "14"  # бамп при изменении промпта запросов -> старый кэш игнорируется и набор пересобирается

def _load_query_set(site):
    try:
        with open(_queries_cache_path(site), encoding="utf-8") as f:
            obj = json.load(f)
        if obj.get("ver") != _QSET_VER:   # промпт обновился -> старый набор не используем
            return None
        qs = obj.get("queries")
        # перелейблируем группы по текущему классификатору (названия групп могли смениться), запросы не трогаем
        return [{"q": x["q"], "group": _classify_query(x["q"])} for x in qs] if qs else None
    except Exception:
        return None

def _save_query_set(site, queries):
    try:
        with open(_queries_cache_path(site), "w", encoding="utf-8") as f:
            json.dump({"ver": _QSET_VER, "date": datetime.datetime.now().strftime("%Y-%m-%d"),
                       "queries": [{"q": x["q"], "group": x["group"]} for x in queries]}, f, ensure_ascii=False)
    except Exception:
        pass

# ───────────────────────── оркестрация ───────────────────────
_TRANSIENT = re.compile(r"(429|500|502|503|504|high demand|temporar|timed out|timeout|unavailable|overload|rate.?limit|"
                        r"connection|reset|disconnect|broken pipe|\bssl\b|eof occurred|remote end)", re.I)
_RATELIMIT = re.compile(r"resource.?exhausted|rate.?limit|too many requests|high demand|per.?minute", re.I)
# Стойкие ошибки: исчерпана квота/нет биллинга/неверный ключ/доступ. Повторять и «спасать» бесполезно —
# только жжём время и остатки квоты. Сразу помечаем сеть как недоступную (клиент увидит честно, Анна — алерт).
_HARDFAIL = re.compile(r"quota|billing|exceeded your current quota|api[_ ]?key not valid|invalid.{0,4}api.?key|"
                       r"permission denied|unauthor|\b401\b|\b403\b|\b404\b|not found", re.I)
def _ask_one(prompt, eid, run, brand, test, errlog=None, errlock=None):
    if test or not has_key(eid):
        try: return ask_mock(prompt, eid, run, brand)
        except Exception: return None
    max_try = 3 if eid == "gemini" else 2                   # Gemini чаще ловит «high demand» -> на попытку больше (добор — в спас.проходе)
    for attempt in range(1, max_try + 1):                   # повтор при временном сбое (503/429/обрыв и пр.)
        try:
            return REAL_ADAPTERS[eid](prompt)
        except Exception as e:
            msg = str(e)
            print(f"[ask] {eid} ошибка (попытка {attempt}): {type(e).__name__}: {msg[:120]}", flush=True)
            if errlog is not None:
                rec = {"engine": eid, "attempt": attempt, "type": type(e).__name__, "message": msg[:500]}
                if errlock:
                    with errlock:
                        errlog.append(rec)
                else:
                    errlog.append(rec)
            if _HARDFAIL.search(msg):
                return None                                 # квота/биллинг/ключ -> повтор бесполезен, сразу выходим
            if attempt < max_try and _TRANSIENT.search(msg):
                # перегрузка/лимит -> пауза подольше (но умеренная, чтобы отчёт не тянулся), иначе короткая
                time.sleep((3.0 if _RATELIMIT.search(msg) else 1.2) * attempt); continue
            return None                                     # стойкая ошибка (ключ/квота/сеть) -> не «0%», а «не удалось проверить»

def _companies_only(names, niche, corpus="", aliases=None):
    """Единый смысловой фильтр кандидатов в конкуренты: оставить ТОЛЬКО реальные компании/бренды/сайты.
    Подход вместо бесконечных стоп-списков: (1) детерминированно убираем кириллические концепт-слова
    (которые встречаются в ответах со строчной буквы), (2) в проде дополнительно просим нейросеть убрать
    отрасли/тематики/категории/концепты и компании из чужих ниш. Реальные домены и бренды из справочника
    не отдаём на проверку — они точно остаются."""
    names = [n for n in names if n]
    if not names:
        return names
    det = [n for n in names if not _is_common_lowercase(corpus, n)]        # строчные концепты -> вон
    if os.environ.get("TEST_MODE") == "1":
        return det
    def _sure(n):                                                          # точно компания: домен / справочник / ритейлер
        nl = n.lower()
        return bool(_SELF_DOMAIN_RE.match(nl) or nl in _BRAND_SITES or _RETAILERS.get(nl) or _RETAILER_URLS.get(n))
    maybe = [n for n in det if not _sure(n)]
    ask = _first_keyed_adapter()
    if not ask or not maybe:
        return det
    prompt = (f"Ниша бизнеса клиента: «{niche or 'услуги'}».\n"
              "Ниже список кандидатов в конкуренты, автоматически извлечённый из ответов нейросетей; в нём есть и реальные "
              "компании, и мусор. Перечисли ПОСТРОЧНО только те строки, которые НЕ являются реальной отдельной "
              "компанией, брендом, агентством или сайтом, а представляют собой: отрасль или тематику (например «B2B», "
              "«юридическая тематика», «медицинские сайты», «промышленное оборудование»), категорию или тип игроков, общее "
              "понятие, характеристику, подзаголовок, инструкцию, тип разметки, название нейросети или поисковика, либо "
              "компанию из чужой, не связанной с этой нишей отрасли. Пиши строки ровно как во входе, по одной на строку, "
              "без пояснений. Если убирать нечего — ответь «-».\n\n" + "\n".join(maybe))
    try:
        raw = _ask_any(prompt) or ""
    except Exception:
        return det
    bad = set()
    for ln in raw.splitlines():
        s = ln.strip().strip("-*•—\"«».").strip().lower()
        if not s or s == "-":
            continue
        for n in maybe:
            if n.lower() == s or (len(s) >= 5 and (s in n.lower() or n.lower() in s)):
                bad.add(n)
    return [n for n in det if n not in bad]

def _assign_quotes(queries, tasks, answers, engines, aliases):
    """Для каждого запроса выбирает один дословный ответ для блоков «Примеры» и «Развёрнутые ответы».
    Два правила:
      • если по запросу бренд РЕАЛЬНО встречается в чьём-то ответе — показываем ответ именно той сети
        (чтобы клиент видел свой бренд в тексте, а не ответ другой нейросети без бренда);
      • в остальном раздаём примеры РАЗНЫМ нейросетям по очереди, а не одной самой «болтливой»."""
    eng_nm = {e["id"]: e["name"] for e in engines}
    per = {}                                                  # qi -> {eid: (текст, длина, есть_бренд)} — лучший ответ каждой сети
    for (qi, eid, _q, _run), ans in zip(tasks, answers):
        if not ans or len((ans or "").strip()) < 40:
            continue
        hb = detect_mention(ans, aliases)
        d = per.setdefault(qi, {})
        cur = d.get(eid)                                      # для одной сети предпочитаем прогон С брендом, при равенстве — длиннее
        if cur is None or (hb and not cur[2]) or (hb == cur[2] and len(ans) > cur[1]):
            d[eid] = (ans, len(ans), hb)
    used = {}                                                 # сколько раз показали каждую сеть -> равномерно распределяем
    for qi in sorted(per):
        cand = per[qi]
        brand_pool = {e: v for e, v in cand.items() if v[2]}            # сети, где бренд РЕАЛЬНО в ответе — для таких запросов берём их
        pool = brand_pool or ({e: v for e, v in cand.items() if v[1] >= 120} or cand)
        eid = min(pool, key=lambda e: (used.get(e, 0), -pool[e][1]))    # реже всего показанная сеть, при равенстве — длиннее
        used[eid] = used.get(eid, 0) + 1
        queries[qi]["quote"] = {"engine": eng_nm.get(eid, eid), "eid": eid, "text": _clean_quote(pool[eid][0], 1300)}

def _enrich_quotes(queries, aliases, company_names):
    """Помечает в каждой цитате: есть ли бренд и какие из реальных компаний названы ИМЕННО в этом ответе.
    Благодаря этому пример в отчёте всегда согласован — тег и разбор соответствуют показанному тексту цитаты."""
    cl = list(dict.fromkeys([c for c in (company_names or []) if c]))
    for q in queries:
        qt = q.get("quote")
        if not qt or not qt.get("text"):
            continue
        low = qt["text"].lower()
        al = [a for a in (aliases or ()) if a]
        qt["brand"] = detect_mention(qt["text"], aliases)
        qt["named"] = [c for c in cl if c.lower() in low and not any(a in c.lower() for a in al)][:5]  # компании, кроме самого бренда

def _clean_quote(text, limit=320):
    """Чистый короткий ДОСЛОВНЫЙ фрагмент ответа нейросети для блока «Примеры реальных ответов»."""
    t = re.sub(r"\*\*([^*]+)\*\*", r"\1", text or "")            # снять markdown-жирность, оставить слова
    t = re.sub(r"`+|#+|^\s*>+|_{2,}|\[\d+\]|\(https?://[^)]+\)", " ", t)
    t = re.sub(r"^\s*(?:[-*•]|\d+[.)])\s*", "", t)               # ведущий маркер списка
    t = re.sub(r"\s+", " ", t).strip().strip("«»\"'· ")
    if len(t) <= limit:
        return t
    cut = t[:limit]
    last = max(cut.rfind(". "), cut.rfind("! "), cut.rfind("? "))
    if last >= limit * 0.55:                                     # обрезаем по концу предложения, если он близко
        return cut[:last + 1].strip()
    return cut.rsplit(" ", 1)[0].strip() + " …"

def _extract_real_companies(answers, niche, brand="", aliases=None):
    """ОСНОВНОЙ способ: сильная нейросеть сама извлекает РЕАЛЬНЫЕ компании из ответов и даёт их сайты.
    Работает в любой нише без стоп-листов и не выкидывает настоящие компании (модель знает, что МТС — компания,
    а «ВИС»/«метод» — нет). Возвращает [{'name','site'}]; None = модель недоступна (тогда откат на старый способ)."""
    corpus = "\n---\n".join(a for a in answers if a).strip()
    if not corpus:
        return []
    own = ", ".join(sorted({str(x) for x in ([brand] + list(aliases or [])) if x and len(str(x)) >= 3}))
    prompt = (
        f"Ниша клиента: «{niche or 'услуги'}». Сам клиент (его НЕ включать в список): {own or brand or '—'}.\n\n"
        "Ниже — ответы нейросетей на запросы клиентов в этой нише. Найди РЕАЛЬНЫЕ КОМПАНИИ (бренды, агентства, сервисы, "
        "сайты), которых нейросети называют как игроков или исполнителей в этой нише, и для каждой укажи её официальный сайт.\n\n"
        "ВКЛЮЧАЙ только настоящие, узнаваемые компании, которые реально существуют и относятся к этой нише.\n"
        "НЕ ВКЛЮЧАЙ: общие слова и понятия; отрасли, сегменты и категории (например «B2B», «executive search firm», "
        "«кадровые агентства» как класс); инструкции и советы; типы разметки и технические термины; технические файлы "
        "(robots.txt); названия самих нейросетей и поисковиков; аббревиатуры, которые ты не можешь уверенно сопоставить "
        "с конкретной реальной компанией; и самого клиента.\n"
        "Сайт указывай ТОЛЬКО реальный и достоверный (домен компании). Если сайт не знаешь точно — поставь null, НЕ ВЫДУМЫВАЙ.\n"
        "Если не уверена, что строка вообще реальная компания — НЕ включай её. Лучше пропустить сомнительное, чем выдать мусор.\n\n"
        'Ответь СТРОГО в формате JSON-массива и ничего кроме него: '
        '[{"name":"Название","site":"https://domain.ru"},{"name":"Другая","site":null}]\n\n'
        "Ответы нейросетей:\n" + corpus[:8000]
    )
    raw = _ask_any(prompt)
    if not raw:
        return None                                                  # модель недоступна -> откат
    m = re.search(r"\[.*\]", raw, re.S)
    if not m:
        return []
    try:
        arr = json.loads(m.group(0))
    except Exception:
        return []
    own_low = {str(x).lower() for x in ([brand] + list(aliases or [])) if x}
    out, seen = [], set()
    for it in (arr if isinstance(arr, list) else []):
        if not isinstance(it, dict):
            continue
        nm = re.sub(r"\s+", " ", str(it.get("name") or "").strip().strip("«»\"'·")).strip()
        low = nm.lower()
        if not nm or not (2 <= len(nm) <= 60) or low in seen:
            continue
        if re.search(r"[㐀-鿿぀-ヿ가-힯֐-׿؀-ۿ]", nm):           # иероглифы/кана/корейский/иврит/арабский — брак модели
            continue
        if low in own_low or any(a and len(a) >= 5 and (a in low or low in a) for a in own_low):
            continue
        if low in _PLATFORMS or _FILE_EXT_RE.search(low):            # лёгкая страховка: не сама нейросеть и не файл
            continue
        site = it.get("site")
        site = "" if site in (None, "", "null", "None", "-") else str(site).strip()
        if site and not re.match(r"^https?://", site):
            site = "https://" + site.lstrip("/")
        seen.add(low)
        out.append({"name": nm, "site": site})
    return out

def analyze(brand, brand_short, site, niche, city, on_progress=None, site_info=None, aliases=None, queries=None):
    test = os.environ.get("TEST_MODE") == "1"
    aliases = aliases or brand_aliases(site, site_info, brand, brand_short)
    if queries is None:                                      # набор не передали -> кэш или генерация
        queries = None if test else _load_query_set(site)
        if not queries:
            queries = generate_queries(niche, city, site_info, aliases)
            if not test:
                _save_query_set(site, queries)
    queries = [{"q": x["q"], "group": x["group"]} for x in queries]   # чистая копия, без старых hits
    engines = active_engines()
    for q in queries:
        q["hits"] = {e["id"]: 0 for e in engines}
    # все вызовы ко всем сетям считаем параллельно (пачками), а не строго по очереди
    tasks = [(qi, e["id"], q["q"], run)
             for qi, q in enumerate(queries) for e in engines for run in range(RUNS)]
    workers = max(1, min(int(os.environ.get("CONCURRENCY", "8")), len(tasks) or 1))
    errlog = []; errlock = threading.Lock()
    with ThreadPoolExecutor(max_workers=workers) as pool:
        answers = list(pool.map(lambda t: _ask_one(t[2], t[1], t[3], brand, test, errlog, errlock), tasks))
    # ── «спасательный» проход ──────────────────────────────────────────
    # Если сеть в параллельной пачке провалила большинство вызовов — частая причина
    # это упор в лимит запросов в минуту (особенно Gemini на бесплатном тарифе).
    # Повторяем её запросы ПОСЛЕДОВАТЕЛЬНО (с паузами) — так укладываемся в лимит и
    # добираем ответы. Но если повтор не помогает с первой же попытки — ошибка стойкая
    # (ключ/квота/модель), и мы не тратим время на остальные.
    if not test:
        f_tot = {}; f_err = {}
        for (qi, eid, _q, _run), ans in zip(tasks, answers):
            f_tot[eid] = f_tot.get(eid, 0) + 1
            if ans is None: f_err[eid] = f_err.get(eid, 0) + 1
        resc_deadline = time.time() + float(os.environ.get("RESCUE_BUDGET", "75"))  # потолок на спасательный проход, чтобы отчёт не тянулся
        for eid in [e for e in f_tot if f_err.get(e, 0) >= (f_tot[e] + 1) // 2]:
            idxs = [i for i, t in enumerate(tasks) if t[1] == eid and answers[i] is None]
            print(f"[rescue] {eid}: повторяю {len(idxs)} запросов последовательно", flush=True)
            for n, i in enumerate(idxs):
                if time.time() > resc_deadline:  # вышли за бюджет времени -> прекращаем добор (сеть пометится недоступной)
                    print(f"[rescue] {eid}: лимит времени, останавливаю добор", flush=True); break
                _qi, _eid, q, run = tasks[i]
                answers[i] = _ask_one(q, eid, run, brand, test, errlog, errlock)
                if answers[i] is None and n == 0 and eid != "chatgpt":
                    print(f"[rescue] {eid}: повтор не помог — сеть недоступна, прекращаю", flush=True)
                    break                       # стойкая ошибка -> не мучаем остальные
    all_answers = []
    eng_err = {e["id"]: 0 for e in engines}; eng_tot = {e["id"]: 0 for e in engines}
    for (qi, eid, _q, _run), ans in zip(tasks, answers):
        eng_tot[eid] += 1
        if ans is None:                       # ошибка API (квота/ключ/сеть) -> не считаем как ответ
            eng_err[eid] += 1
            continue
        all_answers.append(ans)
        if detect_mention(ans, aliases):
            queries[qi]["hits"][eid] += 1
    failed = [eid for eid in eng_tot if eng_tot[eid] and eng_err[eid] >= (eng_tot[eid] + 1) // 2]   # большинство вызовов с ошибкой -> движок недоступен
    failed_details = {}
    for rec in errlog:
        if rec["engine"] not in failed:
            continue
        arr = failed_details.setdefault(rec["engine"], [])
        if len(arr) < 4:
            arr.append({"type": rec["type"], "message": rec["message"]})
    _assign_quotes(queries, tasks, answers, engines, aliases)  # дословный фрагмент одного ответа на каждый запрос
    # ── ОСНОВНОЙ путь: реальные компании + их сайты извлекает сама нейросеть (семантически, без стоп-листов) ──
    real = None if test else _extract_real_companies(all_answers, niche, brand_short or brand, aliases)
    comp_sites = {}
    for q in queries:
        q["evidence"] = {e["id"]: {"brand": q["hits"][e["id"]] > 0, "comps": [], "others": []} for e in engines}
    if real:                                                          # модель вернула список реальных компаний
        comp_sites = {c["name"]: c["site"] for c in real if c.get("site")}
        names = [c["name"] for c in real]
        freq = {n: sum(1 for a in all_answers if n.lower() in (a or "").lower()) for n in names}
        comp_sorted = sorted([(n, freq[n]) for n in names if freq[n] >= 2], key=lambda x: (-x[1], -len(x[0])))
        competitors = [(_clean_comp_name(n), c) for n, c in comp_sorted[:6]]   # подтверждённые (>=2 ответа)
        comp_low = {n.lower() for n, _ in competitors}
        for (qi, eid, _q, _run), ans in zip(tasks, answers):          # по каждому ответу: какие реальные компании в нём названы
            low = (ans or "").lower(); cell = queries[qi]["evidence"][eid]
            for n in names:
                cn = _clean_comp_name(n)
                if n.lower() in low and cn not in cell["comps"] and cn not in cell["others"]:
                    (cell["comps"] if cn.lower() in comp_low else cell["others"]).append(cn)
        # переносим сайты и на «очищенные» имена-домены (1ps.ru остаётся 1ps.ru и т.п.)
        comp_sites = {_clean_comp_name(k): v for k, v in comp_sites.items()}
        _enrich_quotes(queries, aliases, [_clean_comp_name(n) for n in names])   # бренд и компании ИМЕННО в цитате (для согласованного примера)
        return queries, competitors, failed, comp_sites, failed_details
    # ── ОТКАТ: модель недоступна -> старый способ (regex + фильтры) ──
    competitors = extract_competitors(all_answers, brand, brand_short, niche, aliases=aliases)
    corpus = "\n".join(a for a in all_answers if a)
    comp_names = [n for n, _ in competitors]
    comp_low = {n.lower() for n in comp_names}
    comp_words = {w for n in comp_names for w in n.lower().split()}
    cand = set(comp_names)
    for (qi, eid, _q, _run), ans in zip(tasks, answers):
        low = (ans or "").lower(); cell = queries[qi]["evidence"][eid]
        for n in comp_names:
            if n.lower() in low and n not in cell["comps"]:
                cell["comps"].append(n)
        for n in _named_in_answer(ans, aliases, niche):
            nl = n.lower()
            if nl in comp_low or nl in [o.lower() for o in cell["others"]]: continue
            if " " not in nl and nl in comp_words: continue
            cell["others"].append(n); cand.add(n)
    good_low = {g.lower() for g in _companies_only(sorted(cand), niche, corpus, aliases)}
    for q in queries:
        for e in engines:
            cell = q["evidence"][e["id"]]
            cell["comps"] = [c for c in cell["comps"] if c.lower() in good_low]
            cell["others"] = [o for o in cell["others"] if o.lower() in good_low]
    competitors = [(n, c) for (n, c) in competitors if n.lower() in good_low]
    _enrich_quotes(queries, aliases, [n for n in cand if n.lower() in good_low])   # бренд и компании в цитате для согласованного примера
    return queries, competitors, failed, {}, failed_details

# ───────────────────────── сборка данных отчёта ───────────────────────
def _rates(queries):
    rates = {}
    for e in active_engines():
        m = sum(q["hits"].get(e["id"], 0) for q in queries)
        rates[e["id"]] = round(m / (len(queries)*RUNS) * 100)
    return rates

def _groups(queries):
    g = {}
    ne = len(active_engines())
    for q in queries:
        gg = g.setdefault(q["group"], {"m":0,"mx":0})
        gg["m"] += sum(q["hits"].values()); gg["mx"] += ne*RUNS
    return sorted(([k, round(v["m"]/v["mx"]*100)] for k,v in g.items()), key=lambda x:-x[1])

