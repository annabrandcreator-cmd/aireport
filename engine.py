# Gemini на бесплатном тарифе лимитирован по запросам в минуту (flash-lite ~30/мин).
# Отчёт шлёт ~20 запросов пачкой -> равномерно разносим их во времени, чтобы не ловить 429.
_GEMINI_LOCK = threading.Lock()
_GEMINI_NEXT = [0.0]                                         # самое раннее время следующего вызова
def _gemini_pace():
    gap = float(os.environ.get("GEMINI_MIN_GAP", "0") or 0)  # сек между вызовами Gemini; 0 = выкл (на платном тарифе пауза не нужна)
    if gap <= 0:
        return
    with _GEMINI_LOCK:                                       # держим интервал между запросами ко всем потокам
        now = time.time()
        wait = _GEMINI_NEXT[0] - now
        if wait > 0:
            time.sleep(wait); now = time.time()
        _GEMINI_NEXT[0] = now + gap

# Одна модель Gemini в часы пик часто отдаёт 503 «high demand». Поэтому пробуем по очереди
# несколько моделей: если первая перегружена — берём следующую. Список можно переопределить
# переменной GEMINI_MODEL (через запятую). На перегрузку (503/overload) переходим к следующей,
# на квоту/ключ (429/billing) — нет смысла, выходим сразу.
_GEMINI_CAP = re.compile(r"503|unavailable|high demand|overload|deadline|exceeded.*deadline|temporar", re.I)
def ask_gemini(prompt):
    if not _direct_key("gemini") and _openrouter_key():
        return ask_openrouter("gemini", prompt)
    key = os.environ["GEMINI_API_KEY"]
    models = [m.strip() for m in os.environ.get(
        "GEMINI_MODEL", "gemini-2.5-flash,gemini-2.0-flash,gemini-2.5-flash-lite").split(",") if m.strip()]
    _gemini_pace()
    last = None
    for i, model in enumerate(models):
        url = f"https://generativelanguage.googleapis.com/v1beta/models/{model}:generateContent?key={key}"
        try:
            j = _post_json(url, {"Content-Type": "application/json"},
                           {"contents": [{"parts": [{"text": prompt}]}]})
            return j["candidates"][0]["content"]["parts"][0]["text"]
        except Exception as e:
            last = e
            if i + 1 < len(models) and _GEMINI_CAP.search(str(e)):
                print(f"[gemini] {model} перегружена ({str(e)[:60]}) -> пробую следующую модель", flush=True)
                continue                                     # модель перегружена -> следующая в списке
            raise
    raise last

# GigaChat: ключ из кабинета (Authorization Key, Basic) меняется на access token на 30 минут.
# Сертификаты Минцифры в контейнере не ставим -> отключаем проверку TLS для доменов Сбера.
_GIGA_TOKEN = {"val": "", "exp": 0.0}
_GIGA_LOCK = threading.Lock()
_GIGA_NEXT = [0.0]
def _giga_ctx():
    ctx = ssl.create_default_context()
    ctx.check_hostname = False
    ctx.verify_mode = ssl.CERT_NONE
    return ctx

def _giga_serial_call():
    return os.environ.get("GIGACHAT_SERIAL", "1").strip().lower() not in ("0", "false", "no", "off")

def _giga_min_gap():
    return float(os.environ.get("GIGACHAT_MIN_GAP", "2.5") or 2.5)

def _giga_pace():
    if not _giga_serial_call():
        return
    gap = _giga_min_gap()
    now = time.time()
    wait = _GIGA_NEXT[0] - now
    if wait > 0:
        time.sleep(wait)
        now = time.time()
    _GIGA_NEXT[0] = now + gap

def _giga_token():
    now = time.time()
    if _GIGA_TOKEN["val"] and now < _GIGA_TOKEN["exp"]:
        return _GIGA_TOKEN["val"]
    auth = os.environ["GIGACHAT_API_KEY"]                       # Authorization Key из кабинета
    scope = os.environ.get("GIGACHAT_SCOPE", "GIGACHAT_API_PERS")  # PERS=физлицо, B2B/CORP=юрлицо
    req = urllib.request.Request(
        "https://ngw.devices.sberbank.ru:9443/api/v2/oauth",
        data=f"scope={scope}".encode(), method="POST",
        headers={"Authorization": "Basic " + auth, "RqUID": str(uuid.uuid4()),
                 "Content-Type": "application/x-www-form-urlencoded", "Accept": "application/json"})
    with urllib.request.urlopen(req, timeout=30, context=_giga_ctx()) as r:
        j = json.loads(r.read().decode())
    _GIGA_TOKEN["val"] = j["access_token"]
    _GIGA_TOKEN["exp"] = now + 25 * 60                          # с запасом до истечения (30 мин)
    return _GIGA_TOKEN["val"]

def ask_gigachat(prompt):
    lock = _GIGA_LOCK if _giga_serial_call() else threading.Lock()
    with lock:
        last = None
        for attempt in (1, 2, 3):                              # GigaChat нестабилен: протухший токен/обрыв TLS/429 -> сброс токена и повтор
            try:
                _giga_pace()
                tok = _giga_token()
                req = urllib.request.Request(
                    "https://gigachat.devices.sberbank.ru/api/v1/chat/completions",
                    data=json.dumps({"model": os.environ.get("GIGACHAT_MODEL", "GigaChat"),
                                     "messages": [{"role": "user", "content": prompt}]}).encode(),
                    method="POST",
                    headers={"Authorization": "Bearer " + tok, "Content-Type": "application/json", "Accept": "application/json"})
                with urllib.request.urlopen(req, timeout=40, context=_giga_ctx()) as r:
                    return json.loads(r.read().decode())["choices"][0]["message"]["content"]
            except Exception as e:
                last = e
                _GIGA_TOKEN["val"] = ""; _GIGA_TOKEN["exp"] = 0.0   # сбрасываем токен -> на повторе перелогинимся
                if attempt < 3:
                    time.sleep(8.0 if "429" in str(e) else 1.8); continue
                raise last

