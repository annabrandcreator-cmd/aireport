    out = re.sub(r"\s+и\s+([A-Za-zА-Яа-яЁё-]+)", lambda m: (m.group(0) if keep(m.group(1)) else ""), refined or "")
    return re.sub(r"\s{2,}", " ", out).strip(" ,")

